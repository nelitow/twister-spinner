          <h3>Sobre o jogo Twister</h3>
          <p>Twister é um jogo de habilidade física, produzido pela Milton Bradley Company e pela Winning Moves,
             que foi introduzido no American National Toy Hall of Fame.<br>
             É jogado em um grande tapete de plástico que é espalhado no chão ou no chão.
             O tapete tem seis fileiras de grandes círculos coloridos com uma cor diferente em cada linha: vermelho, amarelo, verde e azul.<br>
             Um spinner é anexado a um tabuleiro quadrado e é usado para determinar onde o jogador deve colocar a mão ou o pé.
             O spinner é dividido em quatro seções rotuladas: pé esquerdo, pé direito, mão esquerda e mão direita.
             Cada uma dessas quatro seções é dividida nas quatro cores (vermelho, amarelo, verde e azul). Que é o que este site substitui.<br>
             Depois de girar, a combinação é chamada (por exemplo: mão direita no amarelo),
             e os jogadores devem mover a mão ou o pé correspondente para um círculo da cor correta.<br>
             Em um jogo para dois jogadores, duas pessoas não podem ter uma mão ou um pé no mesmo círculo.<br>
             Devido à escassez de círculos coloridos, os jogadores frequentemente precisarão se colocar em posições improváveis ​​ou precárias, eventualmente fazendo com que alguém caia.
             Uma pessoa é eliminada quando cai ou quando o cotovelo ou o joelho toca o tapete.<br>
             Não há limite para quantos podem jogar de uma só vez, mas mais de quatro é o ideal.
           </p>
